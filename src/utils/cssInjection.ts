const STYLE_ID = 'launchdarkly-toolbar-styles';

/**
 * Injects the LaunchDarkly Toolbar CSS into the document head
 * Only injects once, even if called multiple times
 */
export function injectCSS(): void {
  // Check if styles are already injected
  if (document.getElementById(STYLE_ID)) {
    return;
  }

  // Create style element
  const styleElement = document.createElement('style');
  styleElement.id = STYLE_ID;
  styleElement.textContent = CSS_CONTENT;

  // Inject into document head
  document.head.appendChild(styleElement);
}

/**
 * Removes the injected CSS from the document
 * Useful for cleanup in tests or when unmounting
 */
export function removeInjectedCSS(): void {
  const styleElement = document.getElementById(STYLE_ID);
  if (styleElement) {
    styleElement.remove();
  }
}
